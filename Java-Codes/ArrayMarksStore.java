public class ArrayMarksStore {
    public static void main(String[] args) {
        int Marks[] = new int[4];
        Marks[0] = 95; 
        Marks[1] = 99;
        Marks[2] = 96;
        Marks[3] = 98; 
        for(int i=0; i<=3; i++){
            System.out.println(Marks[i]);
        }
    }
}
